#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	while (1)
	{
		if ((*lst)->next == NULL)
		{
			(*lst)->next = new;
			return ;
		}
		else
			(*lst) = (*lst)->next;
	}
}
